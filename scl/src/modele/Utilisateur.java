package modele;

public class Utilisateur {

	private int id;
	private String prenom;
	private String nom;
	private int age;
	private boolean sexe;
	
	
	
	public int getId(){
		return id;
	}
	public String getPrenom(){
		return prenom;
	}
	public String getNom(){
		return nom;
	}
	public int getAge(){
		return age;
	}
	public boolean getSexe(){
		return sexe;
	}
	
}
